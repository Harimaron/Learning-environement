function soustraire(a, b) {
    return a - b;
}

function diviser(a, b) {
    return a / b;
}

function multiplier(a, b) {
    return a * b;
}

function pourcentage(valeur, pourcentage) {
    return pourcentage + '% de ' + valeur + ' = ' + (valeur * pourcentage / 100);
}

function vitesse(distanceEnKm, tempsEnH) {
    return (distanceEnKm / tempsEnH) + "km/h";
}

